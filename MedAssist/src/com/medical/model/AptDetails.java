package com.medical.model;

public class AptDetails {
	String  docEmail, medInfo , problem , addProblems, days , patientEmail,dateTime;



	public String getDateTime() {
		return dateTime;
	}

	public void setDateTime(String dateTime) {
		this.dateTime = dateTime;
	}

	public String getDocEmail() {
		return docEmail;
	}

	public void setDocEmail(String docEmail) {
		this.docEmail = docEmail;
	}

	public String getMedInfo() {
		return medInfo;
	}

	public void setMedInfo(String medInfo) {
		this.medInfo = medInfo;
	}

	public String getProblem() {
		return problem;
	}

	public void setProblem(String problem) {
		this.problem = problem;
	}

	public String getAddProblems() {
		return addProblems;
	}

	public void setAddProblems(String addProblems) {
		this.addProblems = addProblems;
	}

	public String getDays() {
		return days;
	}

	public void setDays(String days) {
		this.days = days;
	}

	public String getPatientEmail() {
		return patientEmail;
	}

	public void setPatientEmail(String patientEmail) {
		this.patientEmail = patientEmail;
	}

}
